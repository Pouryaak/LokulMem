const TEMPORAL_TOKENS = new Set([
  'monday',
  'tuesday',
  'wednesday',
  'thursday',
  'friday',
  'saturday',
  'sunday',
  'today',
  'tomorrow',
  'yesterday',
  'morning',
  'afternoon',
  'evening',
  'night',
]);

export function isCommonWord(word: string): boolean {
  return COMMON_WORDS.has(word.toLowerCase());
}

export function isTemporalToken(word: string): boolean {
  const token = word.toLowerCase().replace(/s$/, '');
  return TEMPORAL_TOKENS.has(token);
}

export function normalizeLocationValue(value: string): string {
  return value
    .toLowerCase()
    .replace(/^the\s+/, '')
    .replace(/\s+/g, ' ')
    .trim();
}
